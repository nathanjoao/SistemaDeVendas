public class Pessoa {

	private String nome;
	private String cpf;
	private int rg;
	
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	
	public String getCpf() {
		return cpf;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setRg(int rg) {
		this.rg = rg;
	}
	
	public int getRg() {
		return rg;
	}
}
